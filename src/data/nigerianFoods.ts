export const getFoodsForCondition = (condition: string): Food[] => {
  return NIGERIAN_FOODS.filter(f => f.safeFor.includes(condition as any));
};

export const getFoodById = (id: string): Food | undefined => {
  return NIGERIAN_FOODS.find(f => f.id === id);
};

export const searchFoods = (query: string): Food[] => {
  const q = query.toLowerCase();
  return NIGERIAN_FOODS.filter(
    f =>
      f.name.toLowerCase().includes(q) ||
      (f.localName && f.localName.toLowerCase().includes(q)) ||
      f.description.toLowerCase().includes(q)
  );
};
